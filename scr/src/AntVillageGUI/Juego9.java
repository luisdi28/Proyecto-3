    public void checkIteraciones(){
        if (iteraciones == listanormal.getSize()){
            flag=false;
            iteraciones=0;
            listanormal = null;
        }
    }

    @Override
    public void actionPerformed(ActionEvent e) {

        if (flag == true) {
            if (iteraciones != listanormal.getSize()) {
                x = listanormal.buscarx(iteraciones);
                y = listanormal.buscary(iteraciones);
                if (Gx != x || Gy != y) {
                    if (Gx == x && Gy < y) {
                        Gy += 10;
                        repaint();
                    }
                    if (Gx == x && Gy > y) {
                        Gy -= 10;
                        repaint();
                    }
                    if (Gy == y && Gx < x) {
                        Gx += 10;
                        repaint();
                    }
                    if (Gy == y && Gx > x) {
                        Gx -= 10;
                        repaint();
                    }
                    if (Gx > x && Gy < y) {
                        Gx = Gx - 10;
                        Gy += 10;
                        repaint();
                    } else if (Gx < x && Gy > y) {
                        Gx = Gx + 10;
                        Gy -= 10;
                        repaint();
                    } else if (Gx < x && Gy < y) {
                        Gx += 10;
                        Gy += 10;
                        repaint();
                    } else {
                        Gx -= 10;
                        Gy -= 10;
                        repaint();
                    }
                } else {
                    iteraciones += 1;
                }
            } else {

                System.out.println("el x es:" + Gx + " el y es:" + Gy);
                checkIteraciones();
                flag = false;
                flag_comida = false;
                Comida obj = new Comida("Juego 9", Integer.parseInt(Comida_Esco.getText()), "verde");
                Comida.main();
            }

        }
    }

